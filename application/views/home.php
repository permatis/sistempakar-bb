        <br/>
        <p align="justify">BlackBerry adalah telepon seluler yang memiliki kemampuan layanan push e-mail, telepon, sms, menjelajah internet, BlackBerry Messenger (BBM), dan berbagai kemampuan nirkabel lainnya. Penggunaan smartphone canggih ini begitu fenomenal belakangan ini, sampai menjadi suatu kebutuhan untuk fashion. BlackBerry pertama kali diperkenalkan pada tahun 1999 oleh perusahaan Kanada, BlackBerry. Kemampuannya menyampaikan informasi melalui jaringan data nirkabel dari layanan perusahaan telepon genggam hingga mengejutkan dunia.
     	
        <p align="justify">BlackBerry pertama kali diperkenalkan di Indonesia pada pertengahan Desember 2004 oleh operator Indosat dan perusahaan Starhub. Perusahaan Starhub merupakan pengejewantahan dari RIM yang merupakan rekan utama BlackBerry. Pasar BlackBerry kemudian diramaikan oleh dua operator besar lainnya di tanah air yakni Excelcom dan Telkomsel. Akibat tuntutan pemerintah Indonesia, BlackBerry akhirnya membuka kantor perwakilan di Indonesia pada November 2010.</p>
        
        <p align="justify">Produk yang menjadi andalan utama dan membuat BlackBerry digemari di pasar adalah fitur surat-e cepat (push e-mail). Produk ini mendapat sebutan surat-e cepat karena seluruh surat-e baru, daftar kontak, dan informasi jadwal (calendar) “ditampilkan” langsung ke dalam BlackBerry secara otomatis. Seperti yang telah disebutkan di atas mengenai keunggulan dari BlackBerry, yaitu push e-mail. Dengan push e-mail semua surat-e masuk dapat diteruskan langsung ke ponsel. E-mail juga sudah mengalami proses kompresi dan scan di server BlackBerry sehingga aman dari virus. Lampiran berkas berupa dokumen Microsoft Office dan PDF dapat dibuka dengan mudah. Sebuah surat-e berukuran 1 MB jika diterima melalui push e-mail dapat menjadi 10 KB dengan isi yang tetap.</p>
        
        <p align="justify">Pengguna tidak perlu mengakses Internet terlebih dulu dan membuka satu persatu surat-e yang masuk, atau pemeriksaan surat-e baru. Hal ini dimungkinkan karena pengguna akan terhubung secara terus-menerus dengan dunia maya melalui jaringan telepon seluler yang tersedia. Alat penyimpanan juga memungkinkan para pengguna untuk mengakses data yang sampai ketika berada di luar layanan jangkauan nirkabel. Begitu pengguna terhubung lagi, BlackBerry Enterprise Server akan menyampaikan data terbaru yang masuk.</p>
        
        <p align="justify">Kelebihan lainnya adalah kemampuan BlackBerry yang dapat menampung surat-e hingga puluhan ribu tanpa ada risiko hang, asalkan masih ada memori tersisa. BlackBerry juga bisa digunakan untuk chatting. Mirip dengan Yahoo Messenger yang bernama BlackBerry Messenger (BBM) yang berjalan melalui jaringan BlackBerry dengan memasukan nomor identitas unik atau PIN dari setiap ponsel BlackBerry. Semua layanan BlackBerry ini dikenal sangat aman, baik surat-e, chatting, maupun browsing. Untuk browsing Internet, data-data dari situs web sudah dikompresi sehingga lebih cepat dibuka.</p>
        
        <p align="justify">Fasilitas lain yang menjadi andalan BlackBerry adalah pesan instan. Yahoo Messenger, Google Talk, dan Skype kini telah menjadi rekan BlackBerry. Teknologi terkini memang memungkinkan kita untuk mengobrol (chatting) di Internet melalui telepon genggam dan Personal Digital Assistant (PDA), tetapi yang berbeda pada BlackBerry adalah proses instalasi lengkap yang bisa dilakukannya melalui jaringan nirkabel.</p>
        
        <p align="justify">Melihat fenomena BlackBerry yang digemari masyarakat karena keunggulan fasilitas komunikasinya, membuat banyak perusahaan IT berkembang dan berlomba-lomba menciptakan aplikasi yang paling mutakhir untuk pengguna BlackBerry. Keunggulan lain juga hadir melalui teknologi kompresi yang menyebabkan biaya akses menjadi murah dan pemberitahuan jawaban pesan melalui tanda getar pada BlackBerry. Penggunaan BlackBerry semakin meluas dengan hadirnya fasilitas koneksi BlackBerry (BlackBerry Connect). Dengan BlackBerry Connect, pengguna tidak lagi harus menggunakan perangkat genggam BlackBerry untuk memanfaatkan BlackBerry Internet Solution. Pengguna hanya perlu menginstalasi BlackBerry Connect pada smartphone merek apapun yang dimiliki, sehingga bisa memanfaatkan BlackBerry Internet Solution.</p>
